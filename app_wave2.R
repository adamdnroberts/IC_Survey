            updateActionButton(
              session,
              "geocode_btn",
              label = "Buscar",
              icon = NULL
            )

            output$geocode_result <- renderUI({
              tags$div(
                style = paste0(
                  "color: #856404; margin-top: 10px; padding: 10px; ",
