# Block randomization assignment table (generated by code/block_randomization_vector.R)
assignment_table <- read.csv("assignment_vector.csv", stringsAsFactors = FALSE)

get_assignment_counter <- function(bucket) {
  tryCatch(
    {
      s3 <- paws.storage::s3()
      raw <- s3$get_object(
        Bucket = bucket,
        Key = "block_randomization_counter.json"
      )
      jsonlite::fromJSON(rawToChar(raw$Body))
    },
    error = function(e) {
      list("MORENA/PT/PVEM" = 0L, "PAN/PRI/PRD" = 0L, "MC" = 0L)
    }
  )
}

save_assignment_counter <- function(bucket, counter) {
  tryCatch(
    {
      s3 <- paws.storage::s3()
      body <- charToRaw(jsonlite::toJSON(counter, auto_unbox = TRUE))
      s3$put_object(
        Bucket = bucket,
        Key = "block_randomization_counter.json",
        Body = body
      )
    },
    error = function(e) invisible(NULL)
  )
}

# Claims the next treatment slot for a respondent given their home municipality's coalition.
# coalition_label: value from d_geo$coalition_label ("MORENA/PVEM/PT", "PAN/PRI/PRD", or "MC")
# Falls back to simple random if coalition is unknown or S3 is unavailable.
claim_next_assignment <- function(coalition_label) {
  coal_key <- switch(
    coalition_label,
    "MORENA/PVEM/PT" = "MORENA/PT/PVEM",
    "PAN/PRI/PRD" = "PAN/PRI/PRD",
    "MC" = "MC",
    NULL
  )
  bucket <- Sys.getenv("S3_BUCKET")
  if (is.null(coal_key) || nchar(bucket) == 0) {
    return(sample(c("control", "control2", "T1", "T2", "T3", "T4"), 1))
  }
  counter <- get_assignment_counter(bucket)
  slot_idx <- (counter[[coal_key]] %% 2000L) + 1L
  assignment <- assignment_table$assignment[
    assignment_table$coalition == coal_key & assignment_table$slot == slot_idx
  ]
  if (length(assignment) == 0 || is.na(assignment[1])) {
    return(sample(c("control", "control2", "T1", "T2", "T3", "T4"), 1))
  }
  counter[[coal_key]] <- counter[[coal_key]] + 1L
  save_assignment_counter(bucket, counter)
  assignment[1]
}

# Mahalanobis matching on log(population), log(area_km2), log(distance_km).
# Returns the n closest candidates to home_id by Mahalanobis distance.
# candidates must contain columns: muni_id, POB_TOTAL, area_km2, centroid_lon, centroid_lat.
mahal_match_munis <- function(home_id, candidates, n = 4) {
  if (nrow(candidates) == 0) {
    return(candidates)
  }
  if (nrow(candidates) <= n) {
    return(candidates)
  }

  home_row <- d_geo %>%
    st_drop_geometry() %>%
    filter(muni_id == home_id)

  if (nrow(home_row) == 0) {
    return(candidates[seq_len(n), ])
  }

  # Great-circle distance from home centroid to each candidate centroid (km)
  home_sf <- sf::st_sfc(
    sf::st_point(c(home_row$centroid_lon[1], home_row$centroid_lat[1])),
    crs = 4326
  )
  cand_sf <- sf::st_as_sf(
    candidates,
    coords = c("centroid_lon", "centroid_lat"),
    crs = 4326,
    remove = FALSE
  )
  dist_km <- as.numeric(sf::st_distance(home_sf, cand_sf)) / 1000

  # Covariate matrix (log-transformed)
  X <- cbind(
    log_pop = log(pmax(candidates$POB_TOTAL, 1)),
    log_area = log(pmax(candidates$area_km2, 0.01)),
    log_dist = log(pmax(dist_km, 0.1))
  )

  # Target: home municipality values; distance target = 0.1 km (prefer nearby)
  x0 <- c(
    log_pop = log(max(home_row$POB_TOTAL[1], 1)),
    log_area = log(max(home_row$area_km2[1], 0.01)),
    log_dist = log(0.1)
  )

  cov_X <- cov(X)
  mahal_dist <- tryCatch(
    mahalanobis(X, x0, cov_X),
    error = function(e) rowSums(scale(X)^2)
  )

  candidates[order(mahal_dist)[seq_len(n)], ]
}

